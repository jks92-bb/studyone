public class Ex2 {
    public static void main(String[] args) {
        //25를 a에 저장
        int twentyfive = 25;
        //5를 b에 저장
        int five = 5;
        //36를 c에 저장
        int thirtysix = 36;
        // 4를 d에 저장
        int four = 4;
        // 72를 e에 저장
        int seventytwo = 72;
        //result는 계산의 결과값
        int result;
        result = ((twentyfive * five) + (thirtysix - four) - seventytwo) / five;
        System.out.println(result);

    }
}
